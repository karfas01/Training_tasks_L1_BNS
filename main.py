# 1. Таблица перевода дюймов в сантиметры (10..22)
print("Таблица перевода дюймов в сантиметры")
for i in range(10, 23):
    cm = i * 2.54
    print(f"{i} дюймов = {cm:.2f} см")

print()

# 2. Таблица перевода долларов в рубли по произвольному курсу (1..20)
rate = float(input("Введите курс доллара в рублях: "))
print("Таблица перевода долларов в рубли")
for i in range(1, 21):
    rub = i * rate
    print(f"{i} долларов = {rub:.2f} рублей")

print()

# 3. Вывод двадцати первых четных чисел
print("Двадцать первых четных чисел")
for i in range(1, 21):
    even = 2 * i
    print(even, end=' ')
print("\n")

# 4. Вывод пятнадцати первых нечетных чисел
print("Пятнадцать первых нечетных чисел")
for i in range(15):
    odd = 2 * i + 1
    print(odd, end=' ')
print("\n")

# 5. Стоимость 2..10 кг конфет при произвольной цене за кг
price_per_kg = float(input("Введите цену за 1 кг конфет: "))
print("Стоимость конфет")
for i in range(2, 11):
    cost = i * price_per_kg
    print(f"{i} кг конфет стоит {cost:.2f} рублей")